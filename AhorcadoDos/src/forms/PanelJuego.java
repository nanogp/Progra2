    public void letraClickeada(java.awt.event.ActionEvent evt)
    {
        JButton evento = (JButton) evt.getSource();

        Main.backend.setLetraElegida(evento.getText().charAt(0));

        //liberar evento
        evento.setEnabled(false);
        evento.setContentAreaFilled(false);

        //validar letra
        Main.backend.validarLetraElegida();

        //mostrar letras coincidentes
        actualizarPalabra();

        //actualizar imagen
        actualizarImagen();

        //actualizar stats
        actualizarStats();

        try
        {
            //validar partida
            Main.backend.validarEstadoPartida();

            if (!Main.backend.isPistaMostrada() && Main.backend.getContadorFallos() == Main.backend.getMomentoPista())
            {
                FormDialogo pista = new FormDialogo(null, true, Main.backend.getPalabra().getDefinicion(), FormDialogo.TipoRespuesta.Pista);
                pista.setVisible(true);

                Main.backend.setPistaMostrada(true);
            }
        }
        catch (GanaPartida ex)
        {
            FormDialogo ganaste = new FormDialogo(null, true, "F E L I C I T A C I O N E S ! ! !" + "\n" + "Adivinaste la palabra: " + "\n" + Main.backend.getPalabra().getNombre(), FormDialogo.TipoRespuesta.GanaPartida);
            ganaste.setVisible(true);
            if (ganaste.isDialogResult)
            {
                Main.backend.actualizarEstadisticaUsuario(Main.backend.GANA_PARTIDA);

                //pasar siguiente nivel
                limpiarTodo();
            }
        }
        catch (PierdePartida ex)
        {
            FormDialogo perdiste = new FormDialogo(null, true, "La palabra secreta era: " + "\n" + Main.backend.getPalabra().getNombre() + "\n\n" + "PERDISTE!", (Main.backend.getUsuario().getPuntajeUltimo() > 0) ? FormDialogo.TipoRespuesta.PierdePartidaConPuntos : FormDialogo.TipoRespuesta.PierdePartidaSinPuntos);
            perdiste.setVisible(true);
            if (perdiste.isDialogResult)
            {
                Main.backend.actualizarEstadisticaUsuario(Main.backend.PIERDE_PARTIDA);

                //terminar partida
                Main.backend.setPartidaEnCurso(false);

                //volver a inicio
                this.setVisible(false);
            }

        }

    }

    void limpiarTodo()
    {
        try
        {
            Main.backend.nuevaPartida();
            iniciarTeclado();
            actualizarImagen();
            actualizarStats();
            iniciarPalabra();
        }
        catch (GanaJuego ex)
        {
            FormDialogo ganaJuego = new FormDialogo(null, true, "AGOTASTE LAS PALABRAS Y GANASTE EL JUEGO", FormDialogo.TipoRespuesta.GanaJuego);
            ganaJuego.setVisible(true);
            if (ganaJuego.isDialogResult)
            {
                Main.backend.actualizarRanking();
                Main.backend.setPartidaEnCurso(false);

                //volver a inicio
                this.setVisible(false);
            }
        }

    }

    public void sonido(int tipo)
    {
        String archSonido = "musica//shooting_star.wav";
        this.easterEgg++;

        switch (tipo)
        {
            case 0:
                archSonido = "musica//shooting_star.wav";
                break;
            case 1:
                archSonido = "musica//sonido1.wav";
                break;
            case 4:
                archSonido = "musica//sonido4.wav";
                break;
            case 5:
                archSonido = "musica//sonido5.wav";
                break;
            case 6:
                archSonido = "musica/sonido6.wav";
                break;
            default:
                archSonido = "musica//shooting_star.wav";
                break;

        }

        System.out.println("Reproduccir sonido");
        try
        {
            AudioInputStream audioInputStream = AudioSystem.getAudioInputStream(new File(archSonido).getAbsoluteFile());
            Clip clip = AudioSystem.getClip();
            clip.open(audioInputStream);
            clip.start();
        }
        catch (IOException | UnsupportedAudioFileException | LineUnavailableException e)
        {
            System.out.println(e.getMessage());
        }

    }

